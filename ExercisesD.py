import sys
idx = int(sys.argv[1])-60
myRegexLst = [r"/^((?!010)0|1)*$/", 
            r"/^(?!.*(010|101))[01]*$/",
            r"/^(?=(0|1)+$)\1.*/i", 
            r"/\b(?=(\w)+)(?!\w*\1\w)\w+/i", 
            r"/(?=(\w)+\w*\1)\w*((\w*\1){4}|(?!\1)(\w)\w*\4)\w*/i", 
            r"/\b(?=(\w)+(\w*\1){2})(\1|(\w)(?!\w*\4))+\b/i", 
            r"/\b(((?![aeiou])\w)*([aeiou])(?!\w*\3)){5}\w*/", 
            r"/^((?=1*(01*01*)*01*$)(0|10*1)*|11)+$/", 
            r"/^(0|(1(01*0)*10*)+)$/", 
            r"/^(?!(0|1(01*0)*1)+$)[01]+$/" 
            ]
print(myRegexLst[idx])
